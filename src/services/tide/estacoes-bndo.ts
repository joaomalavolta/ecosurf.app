function haversineKm(lat1: number, lng1: number, lat2: number, lng2: number): number {
  const R = 6371
  const dLat = ((lat2 - lat1) * Math.PI) / 180
  const dLng = ((lng2 - lng1) * Math.PI) / 180
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos((lat1 * Math.PI) / 180) * Math.cos((lat2 * Math.PI) / 180) * Math.sin(dLng / 2) ** 2
  return 2 * R * Math.asin(Math.sqrt(a))
}

/** Estação com constantes reais mais próxima de um ponto. */
export function estacaoBndoMaisProxima(lat: number, lng: number): EstacaoBndo {
  let melhor = ESTACOES_BNDO[0]
  let menor = Infinity
  for (const e of ESTACOES_BNDO) {
    const d = haversineKm(lat, lng, e.lat, e.lng)
    if (d < menor) {
      menor = d
      melhor = e
    }
  }
  return melhor
}
